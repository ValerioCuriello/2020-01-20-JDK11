package it.polito.tdp.artsmia.model;

public class ArtistPeso {
	
	private int artista;
	private int peso;
	
	
	public ArtistPeso(int artista, int peso) {
		super();
		this.artista = artista;
		this.peso = peso;
	}


	public int getArtista() {
		return artista;
	}


	public void setArtista(int artista) {
		this.artista = artista;
	}


	public int getPeso() {
		return peso;
	}


	public void setPeso(int peso) {
		this.peso = peso;
	}
	
	

}
